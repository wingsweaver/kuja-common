package com.wingsweaver.kuja.common.utils.model.tags;

/**
 * 内置的 Tag 数据类型。
 *
 * @author wingsweaver
 */
public interface TagKeyDataTypes {
    /**
     * 任意类型。
     */
    String ANY = "";

    /**
     * 字符串。
     */
    String STRING = "String";

    /**
     * 布尔型。
     */
    String BOOLEAN = "Boolean";

    /**
     * 长整型。
     */
    String LONG = "Long";

    /**
     * 双精度小数。
     */
    String DOUBLE = "Double";

    /**
     * {@link java.util.Date} 类型。
     */
    String DATE = "Date";

    /**
     * 枚举。
     */
    String ENUM = "Enum";

    /**
     * 字节数组。
     */
    String BYTES = "bytes";

    /**
     * 数组。
     */
    String ARRAY = "Array";

    /**
     * 字符串数组。
     */
    String STRING_ARRAY = "StringArray";

    /**
     * 布尔数组。
     */
    String BOOLEAN_ARRAY = "BooleanArray";

    /**
     * 长整型数组。
     */
    String LONG_ARRAY = "LongArray";

    /**
     * 双精度小数数组。
     */
    String DOUBLE_ARRAY = "DoubleArray";

    /**
     * 双精度小数数组。
     */
    String DATE_ARRAY = "DateArray";

    /**
     * 枚举数组。
     */
    String ENUM_ARRAY = "EnumArray";
}
